import { GoogleGenAI } from "@google/genai";

const apiKey = process.env.API_KEY || '';
const ai = new GoogleGenAI({ apiKey });

export const askGeminiConsultant = async (userQuery: string): Promise<string> => {
  if (!apiKey) {
    return "API ключ не настроен. Пожалуйста, свяжитесь с администратором.";
  }

  try {
    const response = await ai.models.generateContent({
      model: 'gemini-2.5-flash',
      contents: userQuery,
      config: {
        systemInstruction: `Ты — профессиональный эксперт-оценщик компании "RadioGold Moscow". 
        Твоя главная цель — помочь клиенту определить радиодетали (КМ конденсаторы, транзисторы, разъемы и т.д.) и убедить его продать их нам.
        
        Правила общения:
        1. Стиль: Вежливый, экспертный, но доступный.
        2. Если спрашивают про цену: Называй примерный диапазон, но ВСЕГДА добавляй, что точная цена зависит от года выпуска и состояния.
        3. Призыв к действию (CTA): В конце ответа предлагай: "Пришлите фото детали для точной оценки" или "Воспользуйтесь калькулятором на сайте".
        4. Преимущества: Упоминай, что мы платим сразу, у нас точные весы и выезд курьера по Москве и области.
        5. Не придумывай: Если не уверен в детали, попроси фото или маркировку.
        6. Контекст: Мы скупаем радиолом на драгметаллы (золото, серебро, платина, палладий).
        
        Отвечай кратко (до 3-4 предложений), чтобы удобно было читать с телефона.`,
        temperature: 0.7,
      }
    });
    
    return response.text || "Извините, я не смог обработать ваш запрос прямо сейчас.";
  } catch (error) {
    console.error("Gemini API Error:", error);
    return "Произошла ошибка связи с сервером ИИ. Попробуйте позже.";
  }
};

export const generateSeoTip = async (): Promise<string> => {
    if (!apiKey) return "Скупаем радиодетали дорого!";
    try {
        const response = await ai.models.generateContent({
            model: 'gemini-2.5-flash',
            contents: "Напиши ровно один короткий (3-6 слов) продающий слоган для сайта скупки радиодеталей. Сразу напиши готовый текст. Не пиши 'Вот вариант', не пиши кавычки.",
            config: {
                temperature: 0.9, // Немного креатива
            }
        });
        const text = response.text?.trim() || "Лучшие цены на радиолом";
        // Если вдруг AI вернул список, берем только первую строку
        return text.split('\n')[0].replace(/["«»]/g, '');
    } catch (e) {
        return "Лучшие цены на радиолом в Москве";
    }
}